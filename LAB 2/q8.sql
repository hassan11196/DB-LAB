SELECT ENAME,HIREDATE,TO_CHAR(SYSDATE,'YYYY') - TO_CHAR(HIREDATE,'YYYY') AS DURATION FROM EMP WHERE TO_CHAR(SYSDATE,'YYYY') - TO_CHAR(HIREDATE,'YYYY') > 35;

