SELECT ENAME,HIREDATE,TO_CHAR(HIREDATE,'DAY') AS WEEK FROM EMP;

--Q11
SELECT INITCAP(ENAME) AS NAME_,LENGTH(ENAME) AS LENGTH_ FROM EMP WHERE SUBSTR(ENAME,0,1) = 'J' OR SUBSTR(ENAME,0,1) = 'M' OR SUBSTR(ENAME,0,1) = 'A';   

